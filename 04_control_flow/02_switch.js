let month = "oct";

switch (month) {
  case "jan":
    console.log("Jan-1");
    break;
  case "feb":
    console.log("Feb-2");
    break;
  case "march":
    console.log("March-3");
    break;
  case "apr":
    console.log("Apr-4");
    break;
  case "may":
    console.log("May-5");
    break;
  case "jun":
    console.log("Jun-6");
    break;
  case "jul":
    console.log("Jul-7");
    break;
  case "aug":
    console.log("Aug-8");
    break;
  case "sep":
    console.log("Sep-9");
    break;
  case "oct":
    console.log("Oct-10");
    break;
  case "nov":
    console.log("Nov-11");
    break;
  case "dec":
    console.log("Dec-12");
    break;

  default:
    console.log("default case matched");
    break;
}
